  readonly colors = {
    complete: '#13b405',
    planned: '#3577dd',
    'in progress': '#d8d81d',
    revised: '#ff0000',
  };

  ngOnInit() {
    if (this.releaseSchedule.length > 2) {
      const headers = this.releaseSchedule[1];
      Object.keys(headers).forEach((k, i) => {
        if (i < 5) {
          // unique values for filter
          const listOfFilter = new Set();

          this.releaseSchedule.forEach((l, j) => {
            l['vehicle_status_color'] = l['field5']
              ? this.colors[l['field5'].toLocaleLowerCase()] ?? ''
              : '';
            if (j > 1) {
              listOfFilter.add(l[k]);
            }
          });

          this.listOfColumns.push({
            name: k,
            displayName: headers[k],
            sortable: true,
            sortOrder: null,
            sortFn: (a: DataItem, b: DataItem) => {
              return a[k].toString().localeCompare(b[k].toString());
            },
            sortDirections: ['ascend', 'descend', null],
            filterMultiple: true,
            listOfFilter: Array.from(listOfFilter).map((v: string) => {
              return { text: v, value: v };
            }),
            filterFn: (list: string[], item: DataItem) => {
              return list.some((v) => item[k].indexOf(v) !== -1);
            },
          });
        } else {
          if (headers[k].toLocaleUpperCase() !== 'STATUS') {
            this.releaseSchedule.forEach((l, j) => {
              if (j > 1) {
                const idx = k.match(/\d+/);
                if (idx) {
                  const netxStatusKey = 'field' + (parseInt(idx[0]) + 1);

                  if (l[netxStatusKey]) {
                    const status = l[netxStatusKey].toLocaleLowerCase();
                    if (status.includes('revised')) {
                      // partial match
                      l['bg_color_' + k] = this.colors['revised'];
                      const s = status.split('(');

                      if (s.length && s[1]) {
                        l[
                          'actual_date_tooltip_' + k
                        ] = `Actual Date : ${s[1].slice(0, -1)}`;
                      }
                    } else if (this.colors[status]) {
                      // exact match
                      l['bg_color_' + k] = this.colors[status];
                    }
                  }
                }
              }
            });

            this.listOfColumns.push({
              name: k,
              displayName: headers[k],
              sortable: false,
              sortOrder: null,
              sortFn: null,
              sortDirections: [],
              filterMultiple: false,
              listOfFilter: null,
              filterFn: null,
            });
          }
        }
      });
    }
  }

